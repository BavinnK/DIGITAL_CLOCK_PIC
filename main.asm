 PROCESSOR 16F887
    #include <p16f887.inc>

__CONFIG _CONFIG1, _HS_OSC & _WDT_OFF & _LVP_OFF & _MCLRE_ON & _PWRTE_ON
__CONFIG _CONFIG2, _BOR4V_BOR40V & _WRT_OFF
CBLOCK  0x190	    ;GENRAL VARIABLES
    COUNTER	    ;0x190 addr
    COUNTER_FLAG
    DISP_COUNTER
    DISP_DIGIT_FLAG
    
ENDC
CBLOCK	0x70	;IENTERRUPT SAVE VARIABLES
    W_TEMP	    ;0x70
    S_TEMP	    ;0x71
    SECOND_DIGIT    ;0x72
    MINUTE_DIGIT    ;0x73
    HOUR_DIGIT	    ;0x74
    SEC_ONES	    ;0x75
    SEC_TENS	    ;0x76
    MIN_ONES	    ;0x77
    MIN_TENS	    ;0x78
    HOUR_ONES	    ;0x79
    HOUR_TENS	    ;0x7A
    TEMP_VAR	    ;0x7B
ENDC
;RESET_VECTOR
    org 0x00
    GOTO MAIN_INIT
;INTERRUPT_VECTOR
    org 0x04
    MOVWF W_TEMP
    SWAPF STATUS,W
    MOVWF S_TEMP
    
    BANKSEL PIR1
    BTFSC PIR1,0
    GOTO TIMER1_INC
    
    ;EACH 1MS we will change what PORTD sends to the displays
    BTFSC INTCON,2
    GOTO TIMER0_LOGIC
    
    
    GOTO ISR_EXIT
    
TIMER1_INC:
    BCF PIR1,0
    BANKSEL COUNTER
    INCF COUNTER,F
    BSF COUNTER_FLAG,0
    GOTO ISR_EXIT
    
TIMER0_LOGIC:
    BCF INTCON,2
    BANKSEL DISP_COUNTER
    INCF DISP_COUNTER,F
    MOVFW DISP_COUNTER
    SUBLW 6
    BTFSS STATUS,2
    CLRF DISP_COUNTER
    CALL DISP_OK
    MOVWF DISP_DIGIT_FLAG
    GOTO ISR_EXIT
    
DISP_OK:
    MOVF DISP_COUNTER,W
    ADDWF PCL,F
    RETLW b'00000001'	;SEC DIGIT ONES
    RETLW b'00000010'	;SEC DIGIT TENS
    RETLW b'00000100'	;MIN DIGIT ONES
    RETLW b'00001000'	;MIN DIGIT TENS
    RETLW b'00010000'	;HR DIGIT ONES
    RETLW b'00100000'	;HR DIGIT TENS
    
    
ISR_EXIT:
    SWAPF S_TEMP,W
    MOVWF STATUS
    SWAPF W_TEMP,F
    SWAPF W_TEMP,W
    RETFIE  
;MAIN_CODE
    org 0x50
MAIN_INIT:
    BANKSEL ANSEL
    CLRF ANSEL
    CLRF ANSELH
    
    BANKSEL TRISD
    CLRF TRISD	    ;USED FOR DISPLAY SHOW
    CLRF TRISB	    ;USED FOR CONTROLLING THE DIGITS
    
    BSF INTCON,7    ;GIE EN
    BSF INTCON,6    ;PEIE EN
    
    
    ;SETUP TIMER1
    BANKSEL PIE1
    BSF PIE1,0	    ;EN TMR1 OVF
    
    BANKSEL T1CON
    BSF T1CON,5	    ;PCS IS SET TO 8
    BSF T1CON,4
    BCF T1CON,1	    ;INTERNAL CLK 
    BSF T1CON,0	    ;TIM1 ON
    ;TIMER1 END
    
    ;TIMER0 SETUP
    BANKSEL OPTION_REG
    BCF OPTION_REG,5
    BCF OPTION_REG,3
    BSF OPTION_REG,2
    BCF OPTION_REG,1
    BCF OPTION_REG,0	    ;32 PCS so every 1.63 ms the timer0 will OVF and ISR will occurs
    
    BSF INTCON,5
    ;TIMER0 END
    GOTO MAIN_LOOP
    
MAIN_LOOP:
    BANKSEL COUNTER_FLAG
    BTFSC COUNTER_FLAG,0
    CALL COUNTER_LOGIC
    
    MOVF SECOND_DIGIT,W
    SUBLW 60
    BTFSC STATUS,2
    CALL SEC_MIN_INC
    
    MOVF MINUTE_DIGIT,W
    SUBLW 60
    BTFSC STATUS,2
    CALL MIN_HR_INC
    
   MOVF HOUR_DIGIT,W
    SUBLW 24
    BTFSC STATUS,2
    CALL HOUR_RESET
    CALL SPLIT
    
    CALL DIGIT_DISP
    GOTO MAIN_LOOP
    
COUNTER_LOGIC:
    BANKSEL COUNTER_FLAG
    CLRF COUNTER_FLAG
    MOVLW 10
    SUBWF COUNTER,W
    BTFSS STATUS,2
    RETURN
    CLRF COUNTER
    INCF SECOND_DIGIT,F
    RETURN

SEC_MIN_INC:
    CLRF SECOND_DIGIT
    INCF MINUTE_DIGIT,F
    RETURN
  
MIN_HR_INC:
    CLRF MINUTE_DIGIT
    INCF HOUR_DIGIT,F
    RETURN
    
HOUR_RESET:
    CLRF HOUR_DIGIT
    RETURN
    
SPLIT:
    ;SECONDS
    CLRF SEC_TENS
    MOVF SECOND_DIGIT,W
    MOVWF TEMP_VAR
SEC_LOOP:
    MOVLW d'10'
    SUBWF TEMP_VAR,F
    BTFSS STATUS,C
    GOTO SEC_DONE
    INCF SEC_TENS,F
    GOTO SEC_LOOP
SEC_DONE:
    MOVLW d'10'
    ADDWF TEMP_VAR,W
    MOVWF SEC_ONES

    ;MINUTES
    CLRF MIN_TENS
    MOVF MINUTE_DIGIT,W
    MOVWF TEMP_VAR
MIN_LOOP:
    MOVLW d'10'
    SUBWF TEMP_VAR,F
    BTFSS STATUS,C
    GOTO MIN_DONE
    INCF MIN_TENS,F
    GOTO MIN_LOOP
MIN_DONE:
    MOVLW d'10'
    ADDWF TEMP_VAR,W
    MOVWF MIN_ONES

    ;HOURS
    CLRF HOUR_TENS
    MOVF HOUR_DIGIT,W
    MOVWF TEMP_VAR
HOUR_LOOP:
    MOVLW d'10'
    SUBWF TEMP_VAR,F
    BTFSS STATUS,C
    GOTO HOUR_DONE
    INCF HOUR_TENS,F
    GOTO HOUR_LOOP
HOUR_DONE:
    MOVLW d'10'
    ADDWF TEMP_VAR,W
    MOVWF HOUR_ONES

    RETURN
    
    
DIGIT_DISP:	    ;to select which digit show the number
    BANKSEL DISP_DIGIT_FLAG
    BTFSC DISP_DIGIT_FLAG,0
    CALL DIGIT_0
    BTFSC DISP_DIGIT_FLAG,1
    CALL DIGIT_1
    BTFSC DISP_DIGIT_FLAG,2
    CALL DIGIT_2
    BTFSC DISP_DIGIT_FLAG,3
    CALL DIGIT_3
    BTFSC DISP_DIGIT_FLAG,4
    CALL DIGIT_4
    BTFSC DISP_DIGIT_FLAG,5
    CALL DIGIT_5
    
    RETURN
    
DIGIT_0:
    BANKSEL DISP_DIGIT_FLAG
    CLRF DISP_DIGIT_FLAG
    MOVF SEC_ONES,W
    CALL SEG_LOOKUP
    XORLW b'11111111'	;since the second ones digit is common anode we have to invert the bits
    BANKSEL PORTD
    MOVWF PORTD
    
    MOVLW b'00111101'
    MOVWF PORTB
    RETURN
    
DIGIT_1:
    BANKSEL DISP_DIGIT_FLAG
    CLRF DISP_DIGIT_FLAG
    MOVF SEC_TENS,W
    CALL SEG_LOOKUP
    XORLW b'11111111'	;since the second TENS digit is common anode we have to invert the bits, same as second ones
    BANKSEL PORTD
    MOVWF PORTD
    MOVLW b'00111110'
    MOVWF PORTB
    RETURN
    
DIGIT_2:
    BANKSEL DISP_DIGIT_FLAG
    CLRF DISP_DIGIT_FLAG
    MOVF MIN_ONES,W
    CALL SEG_LOOKUP
    BANKSEL PORTD
    MOVWF PORTD
    MOVLW b'00111000'
    MOVWF PORTB
    RETURN
    
DIGIT_3:
    BANKSEL DISP_DIGIT_FLAG
    CLRF DISP_DIGIT_FLAG
    MOVF MIN_TENS,W
    CALL SEG_LOOKUP
    BANKSEL PORTD
    MOVWF PORTD
    MOVLW b'00110100'
    MOVWF PORTB
    RETURN
    
DIGIT_4:
    BANKSEL DISP_DIGIT_FLAG
    CLRF DISP_DIGIT_FLAG
    MOVF HOUR_ONES,W
    CALL SEG_LOOKUP
    BANKSEL PORTD
    MOVWF PORTD
    MOVLW b'00101100'
    MOVWF PORTB
    RETURN
    
DIGIT_5:
    BANKSEL DISP_DIGIT_FLAG
    CLRF DISP_DIGIT_FLAG
    MOVF HOUR_TENS,W
    CALL SEG_LOOKUP
    BANKSEL PORTD
    MOVWF PORTD
    MOVLW b'00011100'
    MOVWF PORTB
    RETURN
    
SEG_LOOKUP:
    ADDWF PCL,F
    
    RETLW b'00111111' ;0
    RETLW b'00000110' ;1
    RETLW b'01011011' ;2
    RETLW b'01001111' ;3
    RETLW b'01100110' ;4
    RETLW b'01101101' ;5
    RETLW b'01111101' ;6
    RETLW b'00000111' ;7
    RETLW b'01111111' ;8
    RETLW b'01101111' ;9
END
